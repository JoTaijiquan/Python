print (3+4)         #3+4
print (6-2)         #6-2
print (4*5)         #4x5
print (4/2)         #4 หาร 2
print (3/2)         #3 หาร 2
print (7/3)         #7 หาร 3
print (4%2)         #4 modulus 2 (หารเอาเศษ)
print (8%3)         #8 modulus 3 
print (10//3)        #10 floor division 3 (หารเอาจำนวนเต็ม)
print (2**3)        #2 power 3 (ยกกำลัง)
print (2e3)         #2x10 power 3 (2 คูณ 10 ยกกำลัง 3)
print (2*(10**3))   #2*10 power 3
print (10-2*3)      #10 - (2x3)
print ((10-2)*3)    #(10-2) x 3
print (3*2+1)       #(3x2) +1
